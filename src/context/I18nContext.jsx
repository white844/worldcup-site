// ── Interpolation helper: t("key", { n: 3, city: "Paris" }) ──────────────
function interpolate(str, vars) {
  if (!vars || !str) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLang] = useState("en");

  // Stable t() — only recreated when lang changes, not on every render
  const t = useCallback((key, vars) => {
    const dict = T[lang] ?? T.en;
    const str  = dict[key] ?? T.en[key] ?? key;
    return interpolate(str, vars);
  }, [lang]);

  const dir = lang === "ar" ? "rtl" : "ltr";

  // Memoized context value — prevents all consumers re-rendering on unrelated parent renders
  const value = useMemo(() => ({ lang, setLang, t, dir }), [lang, t, dir]);

  return (
    <I18nContext.Provider value={value}>
      {/* dir-only wrapper — no textAlign override which would break centered hero/trust layouts */}
      <div dir={dir}>
        {children}
      </div>
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}
